SELECT MODEL_YEAR || ' ' || CAR_MAKE || ' ' || CAR_MODEL AS "Cars with no previous owner"
FROM p_cars
WHERE PREVIOUS_OWNER_ID IS NULL
ORDER BY MODEL_YEAR;


--9
SELECT (SUBSTR(FIRST_NAME, 1, 1) || ', ' || LAST_NAME) AS NAME
FROM p_previous_owners
WHERE LOWER(OWNER_CITY) = LOWER(:ENTER_CITY)
ORDER BY LAST_NAME DESC;

--10
SELECT FIRST_NAME || ' ' || LAST_NAME AS "PREVIOUS_OWNER", INITCAP(OWNER_CITY) AS "CITY"
FROM p_previous_owners
WHERE LOWER(OWNER_CITY) LIKE 'b%'
ORDER BY "PREVIOUS_OWNER";

--11
SELECT MODEL_YEAR, CAR_MAKE, CAR_MODEL, CONCAT('$', SELLING_PRICE)
FROM p_cars
WHERE CAR_MAKE IN ('Dodge', 'Nissan', 'Lincoln') AND
SELLING_PRICE BETWEEN 71000 AND 74000
ORDER BY MODEL_YEAR,
CAR_MAKE,
CAR_MODEL,
SELLING_PRICE DESC;

--12
SELECT FIRST_NAME || ' ' || LAST_NAME AS "NAME"
FROM p_previous_owners
WHERE LOWER(FIRST_NAME || '' || LAST_NAME) LIKE LOWER('%at%') 
ORDER BY LAST_NAME;

