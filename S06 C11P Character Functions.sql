SELECT DEALER_NAME AS "Dealer", UPPER(DEALER_STREET || ' ' || DEALER_CITY || ', ' || DEALER_STATE || ' ' || DEALER_ZIP) AS "Address"
FROM p_dealers
WHERE DEALER_ID IN(101, 407, 120)
ORDER BY "Dealer";

--2
SELECT LOWER(LAST_NAME || ', ' || FIRST_NAME) AS "Previous Owner", LOWER(OWNER_STREET || ' ' || OWNER_CITY || ', ' || OWNER_STATE || ' ' || OWNER_ZIP) AS "Address"
FROM p_previous_owners
WHERE OWNER_ID BETWEEN 1010 AND 1015
ORDER BY "Previous Owner";

--3
SELECT CONCAT('Previous Owner : ', UPPER(LAST_NAME)) AS "Previous Owners"
FROM p_previous_owners
WHERE OWNER_STATE = 'MI' AND OWNER_CITY = 'Ann Arbor'
ORDER BY LAST_NAME;

--4
SELECT DISTINCT UPPER(OWNER_CITY) AS "CITY", LENGTH(OWNER_CITY) AS "CITY_LENGTH"
FROM p_previous_owners
ORDER BY "CITY";

--5
